//! Bootstrap golden test snapshots from the reference corpus (74 files).
//!
//! This tool:
//! 1. Walks corpus/reference/
//! 2. Parses each .cha file with tree-sitter
//! 3. Generates a parameterized test file (tests/generated/reference_corpus.rs)
//! 4. Snapshots are created via `cargo insta review`
//!
//! # Related CHAT Manual Sections
//!
//! - <https://talkbank.org/0info/manuals/CHAT.html#File_Format>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Main_Tier>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Dependent_Tiers>

use std::fs;
use std::path::Path;
use std::path::PathBuf;
use walkdir::WalkDir;

use talkbank_parser_tests::test_error::TestError;

/// Entry point for this binary target.
fn main() -> Result<(), TestError> {
    let corpus_root = Path::new("corpus/reference");

    if !corpus_root.exists() {
        return Err(TestError::Failure(format!(
            "Reference corpus not found at {}",
            corpus_root.display()
        )));
    }

    println!(
        "Bootstrapping reference corpus from: {}",
        corpus_root.display()
    );

    // Collect all .cha files
    let mut cha_files: Vec<String> = Vec::new();

    for entry in WalkDir::new(corpus_root).into_iter() {
        let entry = entry
            .map_err(|err| TestError::Failure(format!("Failed to read corpus entry: {err}")))?;
        if entry.path().extension().and_then(|s| s.to_str()) != Some("cha") {
            continue;
        }
        let path = entry.path();
        let relative = relative_path(corpus_root, path).ok_or_else(|| {
            TestError::Failure(format!(
                "Failed to compute relative path from {}",
                path.display()
            ))
        })?;
        let relative_str = relative
            .to_str()
            .ok_or_else(|| TestError::Failure(format!("Invalid path {}", relative.display())))?;
        cha_files.push(format!("corpus/reference/{}", relative_str));
    }

    cha_files.sort();

    println!("Found {} .cha files", cha_files.len());

    // Generate test file with rstest parameterization
    let generated_dir = Path::new("crates/talkbank-parser-tests/tests/generated");
    fs::create_dir_all(generated_dir)?;

    generate_reference_corpus_test(generated_dir, &cha_files)?;

    println!("Bootstrap complete. Run `cargo insta review` to approve snapshots.");
    Ok(())
}

/// Compute a path relative to the corpus root.
fn relative_path(root: &Path, path: &Path) -> Option<PathBuf> {
    let mut root_iter = root.components();
    let mut path_iter = path.components();

    loop {
        match (root_iter.next(), path_iter.next()) {
            (Some(root_component), Some(path_component)) => {
                if root_component != path_component {
                    return None;
                }
            }
            (None, Some(path_component)) => {
                let mut relative = PathBuf::new();
                relative.push(path_component.as_os_str());
                for component in path_iter {
                    relative.push(component.as_os_str());
                }
                return Some(relative);
            }
            (None, None) => return Some(PathBuf::new()),
            (Some(_), None) => return None,
        }
    }
}

/// Generates reference corpus test.
fn generate_reference_corpus_test(generated_dir: &Path, files: &[String]) -> Result<(), TestError> {
    let filepath = generated_dir.join("reference_corpus.rs");

    let mut test_code = String::new();
    test_code.push_str("// This file is auto-generated by bootstrap_reference_corpus\n");
    test_code.push_str("// Do not edit manually\n\n");
    test_code.push_str("use rstest::rstest;\n");
    test_code.push_str("use talkbank_parser_tests::snapshot::*;\n\n");

    test_code.push_str("#[rstest]\n");

    // Generate case attributes using filename stems as identifiers
    let rust_keywords: std::collections::HashSet<&str> = [
        "as", "break", "const", "continue", "crate", "else", "enum", "extern", "false", "fn",
        "for", "if", "impl", "in", "let", "loop", "match", "mod", "move", "mut", "pub", "ref",
        "return", "self", "Self", "static", "struct", "super", "trait", "true", "type", "unsafe",
        "use", "where", "while", "async", "await", "dyn", "abstract", "become", "box", "do",
        "final", "macro", "override", "priv", "typeof", "unsized", "virtual", "yield", "try",
    ]
    .into_iter()
    .collect();

    for file in files {
        let stem = std::path::Path::new(file)
            .file_stem()
            .and_then(|s| s.to_str())
            .unwrap_or("unknown");
        // Convert to valid snake_case Rust identifier
        let mut ident: String = stem
            .to_ascii_lowercase()
            .chars()
            .map(|c| {
                if c.is_ascii_alphanumeric() || c == '_' {
                    c
                } else {
                    '_'
                }
            })
            .collect();
        // Prefix if starts with a digit or is a Rust keyword
        if ident.starts_with(|c: char| c.is_ascii_digit()) {
            ident.insert_str(0, "f_");
        } else if rust_keywords.contains(ident.as_str()) {
            ident.insert_str(0, "k_");
        }
        test_code.push_str(&format!("#[case::{}({:?})]\n", ident, file));
    }

    test_code.push_str(
        "fn reference_corpus_test(#[case] path: &str) -> Result<(), talkbank_parser_tests::test_error::TestError> {\n",
    );
    test_code.push_str("    reference_file_test(path)?;\n");
    test_code.push_str("    Ok(())\n");
    test_code.push_str("}\n");

    fs::write(&filepath, test_code)?;
    println!("Generated: {}", filepath.display());
    println!("  {} test cases", files.len());
    Ok(())
}
