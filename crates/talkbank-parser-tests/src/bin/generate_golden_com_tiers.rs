//! Extract golden %com tier lines from reference corpus
//!
//! This binary extracts all unique %com tier content from the reference corpus
//! (corpus/reference/**/*.cha) and writes them to golden_com_tiers.txt.
//!
//! Similar to golden_words.txt, this provides a comprehensive test oracle for
//! %com tier parsing equivalence between parsers.
//!
//! # Related CHAT Manual Sections
//!
//! - <https://talkbank.org/0info/manuals/CHAT.html#File_Format>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Main_Tier>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Dependent_Tiers>

use clap::Parser;
use shellexpand::tilde;
use std::{
    collections::BTreeSet,
    error::Error,
    fs::{self, File},
    io::Write,
    path::PathBuf,
};
use walkdir::WalkDir;

use talkbank_model::dependent_tier::DependentTier;
use talkbank_model::model::{ChatFile, ComTier, Line, WriteChat};
use talkbank_parser::TreeSitterParser;

/// Extract just the content portion of a ComTier (after %com:\t).
///
/// The ChatParser::parse_com_tier method expects content only, not the full tier line.
/// We serialize the full tier and strip the `%com:\t` prefix.
fn com_tier_content(tier: &ComTier) -> String {
    let mut full = String::new();
    if let Err(err) = tier.write_chat(&mut full) {
        eprintln!("Failed to write com tier: {err}");
        return String::new();
    }

    // Strip the "%com:\t" prefix to get content only
    full.strip_prefix("%com:\t").unwrap_or(&full).to_string()
}

/// Generate the shared golden %com tier list from a reference corpus.
#[derive(Parser, Debug)]
#[command(
    name = "generate-golden-com-tiers",
    about = "Extract known-good %com tiers from CHAT data modeled by the tree-sitter parser"
)]
/// Command-line arguments for this tool.
struct Args {
    /// Directory containing reference CHAT files (default: corpus/reference).
    #[arg(long, default_value = "corpus/reference")]
    corpus_root: String,

    /// Destination file for the golden %com tier list.
    #[arg(long, default_value = "talkbank-parser-tests/golden_com_tiers.txt")]
    output: PathBuf,

    /// Stop after collecting this many unique %com tiers.
    #[arg(long)]
    limit: Option<usize>,
}

/// Entry point for this binary target.
fn main() -> Result<(), Box<dyn Error>> {
    let args = Args::parse();
    let corpus_root = PathBuf::from(tilde(&args.corpus_root).to_string());

    if !corpus_root.is_dir() {
        return Err(format!(
            "Reference corpus directory does not exist: {}",
            corpus_root.display()
        )
        .into());
    }

    let parser =
        TreeSitterParser::new().map_err(|err| format!("failed to initialize parser: {err}"))?;

    let limit = match args.limit {
        Some(limit) => limit,
        None => usize::MAX,
    };
    let mut com_tiers = BTreeSet::new();
    let mut file_count = 0;
    let mut error_count = 0;

    for entry in WalkDir::new(&corpus_root)
        .into_iter()
        .filter_map(Result::ok)
        .filter(|entry| entry.file_type().is_file())
        .filter(
            |entry| match entry.path().extension().and_then(|ext| ext.to_str()) {
                Some(ext) => ext.eq_ignore_ascii_case("cha"),
                None => false,
            },
        )
    {
        if com_tiers.len() >= limit {
            break;
        }

        let path = entry.path();
        file_count += 1;

        let content = match fs::read_to_string(path) {
            Ok(content) => content,
            Err(err) => {
                eprintln!("skipping {}: {err}", path.display());
                error_count += 1;
                continue;
            }
        };

        match parser.parse_chat_file(&content) {
            Ok(chat) => {
                insert_com_tiers_from_chat(&chat, &mut com_tiers, limit);
            }
            Err(errs) => {
                eprintln!("parse errors in {}: {errs}", path.display());
                error_count += 1;
            }
        }
    }

    if let Some(parent) = args.output.parent() {
        fs::create_dir_all(parent)?;
    }

    let mut file = File::create(&args.output)?;
    writeln!(
        file,
        "# Generated by `cargo run --bin generate-golden-com-tiers -- --corpus-root {}`",
        args.corpus_root
    )?;

    for com_tier_line in com_tiers.iter().take(limit) {
        writeln!(file, "{com_tier_line}")?;
    }

    println!("\n=== Golden %com Tier Extraction Complete ===");
    println!("   Files processed: {}", file_count);
    println!("   Errors: {}", error_count);
    println!(
        "   Golden %com tier list written to {} ({} tiers)",
        args.output.display(),
        com_tiers.len().min(limit)
    );

    Ok(())
}

/// Extract %com tiers from a parsed ChatFile and insert serialized versions into collector.
fn insert_com_tiers_from_chat(chat: &ChatFile, collector: &mut BTreeSet<String>, limit: usize) {
    for line in chat.lines.iter() {
        if collector.len() >= limit {
            break;
        }

        if let Line::Utterance(utterance) = line {
            // An utterance can have multiple %com tiers
            for tier in &utterance.dependent_tiers {
                if collector.len() >= limit {
                    break;
                }

                if let DependentTier::Com(com_tier) = tier {
                    let content = com_tier_content(com_tier);
                    if !content.is_empty() {
                        collector.insert(content);
                    }
                }
            }
        }
    }
}
