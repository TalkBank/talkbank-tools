//! Extract golden main tier lines from reference corpus
//!
//! This binary extracts all unique main tier lines from the reference corpus
//! (corpus/reference/**/*.cha) and writes them to golden_main_tiers.txt.
//!
//! Similar to golden_words.txt, this provides a comprehensive test oracle for
//! main tier parsing equivalence between parsers.
//!
//! # Related CHAT Manual Sections
//!
//! - <https://talkbank.org/0info/manuals/CHAT.html#File_Format>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Main_Tier>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Dependent_Tiers>

use clap::Parser;
use shellexpand::tilde;
use std::{
    collections::BTreeSet,
    error::Error,
    fs::{self, File},
    io::Write,
    path::PathBuf,
};
use walkdir::WalkDir;

use talkbank_model::model::{ChatFile, Line};
use talkbank_parser::TreeSitterParser;

/// Generate the shared golden main tier list from a reference corpus.
#[derive(Parser, Debug)]
#[command(
    name = "generate-golden-main-tiers",
    about = "Extract known-good main tiers from CHAT data modeled by the tree-sitter parser"
)]
/// Command-line arguments for this tool.
struct Args {
    /// Directory containing reference CHAT files (default: corpus/reference).
    #[arg(long, default_value = "corpus/reference")]
    corpus_root: String,

    /// Destination file for the golden main tier list.
    #[arg(long, default_value = "talkbank-parser-tests/golden_main_tiers.txt")]
    output: PathBuf,

    /// Stop after collecting this many unique main tiers.
    #[arg(long)]
    limit: Option<usize>,
}

/// Entry point for this binary target.
fn main() -> Result<(), Box<dyn Error>> {
    let args = Args::parse();
    let corpus_root = PathBuf::from(tilde(&args.corpus_root).to_string());

    if !corpus_root.is_dir() {
        return Err(format!(
            "Reference corpus directory does not exist: {}",
            corpus_root.display()
        )
        .into());
    }

    let parser =
        TreeSitterParser::new().map_err(|err| format!("failed to initialize parser: {err}"))?;

    let limit = match args.limit {
        Some(limit) => limit,
        None => usize::MAX,
    };
    let mut main_tiers = BTreeSet::new();
    let mut file_count = 0;
    let mut error_count = 0;

    for entry in WalkDir::new(&corpus_root)
        .into_iter()
        .filter_map(Result::ok)
        .filter(|entry| entry.file_type().is_file())
        .filter(
            |entry| match entry.path().extension().and_then(|ext| ext.to_str()) {
                Some(ext) => ext.eq_ignore_ascii_case("cha"),
                None => false,
            },
        )
    {
        if main_tiers.len() >= limit {
            break;
        }

        let path = entry.path();
        file_count += 1;

        let content = match fs::read_to_string(path) {
            Ok(content) => content,
            Err(err) => {
                eprintln!("skipping {}: {err}", path.display());
                error_count += 1;
                continue;
            }
        };

        match parser.parse_chat_file(&content) {
            Ok(chat) => {
                insert_main_tiers_from_chat(&chat, &mut main_tiers, limit);
            }
            Err(errs) => {
                eprintln!("parse errors in {}: {errs}", path.display());
                error_count += 1;
            }
        }
    }

    if let Some(parent) = args.output.parent() {
        fs::create_dir_all(parent)?;
    }

    let mut file = File::create(&args.output)?;
    writeln!(
        file,
        "# Generated by `cargo run --bin generate-golden-main-tiers -- --corpus-root {}`",
        args.corpus_root
    )?;

    for main_tier_line in main_tiers.iter().take(limit) {
        writeln!(file, "{main_tier_line}")?;
    }

    println!("\n✅ Extraction complete!");
    println!("   Files processed: {}", file_count);
    println!("   Errors: {}", error_count);
    println!(
        "   Golden main tier list written to {} ({} tiers)",
        args.output.display(),
        main_tiers.len().min(limit)
    );

    Ok(())
}

/// Insert main-tier text from parsed utterances into the set.
fn insert_main_tiers_from_chat(chat: &ChatFile, collector: &mut BTreeSet<String>, limit: usize) {
    for line in chat.lines.iter() {
        if collector.len() >= limit {
            break;
        }

        if let Line::Utterance(utterance) = line {
            // Serialize main tier to CHAT format
            let main_tier_line = utterance.main.to_chat();
            collector.insert(main_tier_line);
        }
    }
}
