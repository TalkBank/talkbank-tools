//! Extract golden %mor tier lines from reference corpus
//!
//! This binary extracts all unique %mor tier lines from the reference corpus
//! (corpus/reference/**/*.cha) and writes them to golden_mor_tiers.txt.
//!
//! Similar to golden_words.txt, this provides a comprehensive test oracle for
//! %mor tier parsing equivalence between parsers.
//!
//! # Related CHAT Manual Sections
//!
//! - <https://talkbank.org/0info/manuals/CHAT.html#File_Format>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Main_Tier>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Dependent_Tiers>

use clap::Parser;
use shellexpand::tilde;
use std::{
    collections::BTreeSet,
    error::Error,
    fs::{self, File},
    io::Write,
    path::PathBuf,
};
use walkdir::WalkDir;

use talkbank_model::dependent_tier::MorTier;
use talkbank_model::model::{ChatFile, Line};
use talkbank_parser::TreeSitterParser;

/// Extract just the content portion of a MorTier (after %mor:\t).
///
/// The ChatParser::parse_mor_tier method expects content only, not the full tier line.
fn mor_tier_content(tier: &MorTier) -> String {
    let mut content = String::new();

    for (i, item) in tier.items.iter().enumerate() {
        if i > 0 {
            content.push(' ');
        }
        if let Err(err) = item.write_chat(&mut content) {
            eprintln!("Failed to write mor item: {err}");
        }
    }

    // Write terminator if present
    if let Some(ref term) = tier.terminator {
        if !tier.items.is_empty() {
            content.push(' ');
        }
        content.push_str(term);
    }

    content
}

/// Generate the shared golden %mor tier list from a reference corpus.
#[derive(Parser, Debug)]
#[command(
    name = "generate-golden-mor-tiers",
    about = "Extract known-good %mor tiers from CHAT data modeled by the tree-sitter parser"
)]
/// Command-line arguments for this tool.
struct Args {
    /// Directory containing reference CHAT files (default: corpus/reference).
    #[arg(long, default_value = "corpus/reference")]
    corpus_root: String,

    /// Destination file for the golden %mor tier list.
    #[arg(
        long,
        default_value = "crates/talkbank-parser-tests/golden_mor_tiers.txt"
    )]
    output: PathBuf,

    /// Stop after collecting this many unique %mor tiers.
    #[arg(long)]
    limit: Option<usize>,
}

/// Entry point for this binary target.
fn main() -> Result<(), Box<dyn Error>> {
    let args = Args::parse();
    let corpus_root = PathBuf::from(tilde(&args.corpus_root).to_string());

    if !corpus_root.is_dir() {
        return Err(format!(
            "Reference corpus directory does not exist: {}",
            corpus_root.display()
        )
        .into());
    }

    let parser =
        TreeSitterParser::new().map_err(|err| format!("failed to initialize parser: {err}"))?;

    let limit = match args.limit {
        Some(limit) => limit,
        None => usize::MAX,
    };
    let mut mor_tiers = BTreeSet::new();
    let mut file_count = 0;
    let mut error_count = 0;

    for entry in WalkDir::new(&corpus_root)
        .into_iter()
        .filter_map(Result::ok)
        .filter(|entry| entry.file_type().is_file())
        .filter(
            |entry| match entry.path().extension().and_then(|ext| ext.to_str()) {
                Some(ext) => ext.eq_ignore_ascii_case("cha"),
                None => false,
            },
        )
    {
        if mor_tiers.len() >= limit {
            break;
        }

        let path = entry.path();
        file_count += 1;

        let content = match fs::read_to_string(path) {
            Ok(content) => content,
            Err(err) => {
                eprintln!("skipping {}: {err}", path.display());
                error_count += 1;
                continue;
            }
        };

        match parser.parse_chat_file(&content) {
            Ok(chat) => {
                insert_mor_tiers_from_chat(&chat, &mut mor_tiers, limit);
            }
            Err(errs) => {
                eprintln!("parse errors in {}: {errs}", path.display());
                error_count += 1;
            }
        }
    }

    if let Some(parent) = args.output.parent() {
        fs::create_dir_all(parent)?;
    }

    let mut file = File::create(&args.output)?;
    writeln!(
        file,
        "# Generated by `cargo run --bin generate-golden-mor-tiers -- --corpus-root {}`",
        args.corpus_root
    )?;

    for mor_tier_line in mor_tiers.iter().take(limit) {
        writeln!(file, "{mor_tier_line}")?;
    }

    println!("\n=== Golden %mor Tier Extraction Complete ===");
    println!("   Files processed: {}", file_count);
    println!("   Errors: {}", error_count);
    println!(
        "   Golden %mor tier list written to {} ({} tiers)",
        args.output.display(),
        mor_tiers.len().min(limit)
    );

    Ok(())
}

/// Extract %mor tier content from a parsed ChatFile and insert into collector.
///
/// Only the content portion (after `%mor:\t`) is extracted, not the full tier line.
/// This matches the expected input format for `ChatParser::parse_mor_tier`.
fn insert_mor_tiers_from_chat(chat: &ChatFile, collector: &mut BTreeSet<String>, limit: usize) {
    for line in chat.lines.iter() {
        if collector.len() >= limit {
            break;
        }

        if let Line::Utterance(utterance) = line {
            if let Some(tier) = utterance.mor_tier() {
                let content = mor_tier_content(tier);
                if !content.is_empty() {
                    collector.insert(content);
                }
            }
            if collector.len() >= limit {
                break;
            }
        }
    }
}
