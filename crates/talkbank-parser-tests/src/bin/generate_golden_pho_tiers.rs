//! Extract golden %pho tier lines from reference corpus
//!
//! This binary extracts all unique %pho tier lines from the reference corpus
//! (corpus/reference/**/*.cha) and writes them to golden_pho_tiers.txt.
//!
//! Similar to golden_words.txt, this provides a comprehensive test oracle for
//! %pho tier parsing equivalence between parsers.
//!
//! # Related CHAT Manual Sections
//!
//! - <https://talkbank.org/0info/manuals/CHAT.html#File_Format>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Main_Tier>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Dependent_Tiers>

use clap::Parser;
use shellexpand::tilde;
use std::{
    collections::BTreeSet,
    error::Error,
    fs::{self, File},
    io::Write,
    path::PathBuf,
};
use walkdir::WalkDir;

use talkbank_model::dependent_tier::DependentTier;
use talkbank_model::model::{ChatFile, Line, PhoItem, PhoTier, PhoTierType, WriteChat};
use talkbank_parser::TreeSitterParser;

/// Extract just the content portion of a PhoTier (after %pho:\t).
///
/// The ChatParser::parse_pho_tier method expects content only, not the full tier line.
fn pho_tier_content(tier: &PhoTier) -> String {
    let mut content = String::new();

    for (i, item) in tier.items.iter().enumerate() {
        if i > 0 {
            content.push(' ');
        }
        if let Err(err) = item.write_chat(&mut content) {
            eprintln!("Failed to write pho item: {err}");
        }
    }

    content
}

/// Render `%pho` items as content-only tier text.
fn pho_items_content(items: &[PhoItem]) -> String {
    let tier = PhoTier::new(PhoTierType::Pho, items.to_vec());
    pho_tier_content(&tier)
}

/// Generate the shared golden %pho tier list from a reference corpus.
#[derive(Parser, Debug)]
#[command(
    name = "generate-golden-pho-tiers",
    about = "Extract known-good %pho tiers from CHAT data modeled by the tree-sitter parser"
)]
/// Command-line arguments for this tool.
struct Args {
    /// Directory containing reference CHAT files (default: corpus/reference).
    #[arg(long, default_value = "corpus/reference")]
    corpus_root: String,

    /// Destination file for the golden %pho tier list.
    #[arg(long, default_value = "talkbank-parser-tests/golden_pho_tiers.txt")]
    output: PathBuf,

    /// Stop after collecting this many unique %pho tiers.
    #[arg(long)]
    limit: Option<usize>,
}

/// Entry point for this binary target.
fn main() -> Result<(), Box<dyn Error>> {
    let args = Args::parse();
    let corpus_root = PathBuf::from(tilde(&args.corpus_root).to_string());

    if !corpus_root.is_dir() {
        return Err(format!(
            "Reference corpus directory does not exist: {}",
            corpus_root.display()
        )
        .into());
    }

    let parser =
        TreeSitterParser::new().map_err(|err| format!("failed to initialize parser: {err}"))?;

    let limit = match args.limit {
        Some(limit) => limit,
        None => usize::MAX,
    };
    let mut pho_tiers = BTreeSet::new();
    let mut file_count = 0;
    let mut error_count = 0;

    for entry in WalkDir::new(&corpus_root)
        .into_iter()
        .filter_map(Result::ok)
        .filter(|entry| entry.file_type().is_file())
        .filter(
            |entry| match entry.path().extension().and_then(|ext| ext.to_str()) {
                Some(ext) => ext.eq_ignore_ascii_case("cha"),
                None => false,
            },
        )
    {
        if pho_tiers.len() >= limit {
            break;
        }

        let path = entry.path();
        file_count += 1;

        let content = match fs::read_to_string(path) {
            Ok(content) => content,
            Err(err) => {
                eprintln!("skipping {}: {err}", path.display());
                error_count += 1;
                continue;
            }
        };

        match parser.parse_chat_file(&content) {
            Ok(chat) => {
                insert_pho_tiers_from_chat(&chat, &mut pho_tiers, limit);
            }
            Err(errs) => {
                eprintln!("parse errors in {}: {errs}", path.display());
                error_count += 1;
            }
        }
    }

    if let Some(parent) = args.output.parent() {
        fs::create_dir_all(parent)?;
    }

    let mut file = File::create(&args.output)?;
    writeln!(
        file,
        "# Generated by `cargo run --bin generate-golden-pho-tiers -- --corpus-root {}`",
        args.corpus_root
    )?;

    for pho_tier_line in pho_tiers.iter().take(limit) {
        writeln!(file, "{pho_tier_line}")?;
    }

    println!("\n=== Golden %pho Tier Extraction Complete ===");
    println!("   Files processed: {}", file_count);
    println!("   Errors: {}", error_count);
    println!(
        "   Golden %pho tier list written to {} ({} tiers)",
        args.output.display(),
        pho_tiers.len().min(limit)
    );

    Ok(())
}

/// Extract %pho tiers from a parsed ChatFile and insert serialized versions into collector.
fn insert_pho_tiers_from_chat(chat: &ChatFile, collector: &mut BTreeSet<String>, limit: usize) {
    for line in chat.lines.iter() {
        if collector.len() >= limit {
            break;
        }

        if let Line::Utterance(utterance) = line {
            for dependent in &utterance.dependent_tiers {
                if collector.len() >= limit {
                    break;
                }

                // Extract PhoTier from DependentTier variants that use PhoTier
                let pho_tier_content = match dependent {
                    DependentTier::Pho(tier) => Some(pho_items_content(&tier.items)),
                    DependentTier::Mod(tier) => Some(pho_items_content(&tier.items)),
                    _ => None,
                };

                if let Some(content) = pho_tier_content {
                    if !content.is_empty() {
                        collector.insert(content);
                    }
                }
            }
        }
    }
}
