//! Extract golden %wor tier lines from reference corpus
//!
//! This binary extracts all unique %wor tier content from the reference corpus
//! (corpus/reference/**/*.cha) and writes them to golden_wor_tiers.txt.
//!
//! Similar to golden_words.txt, this provides a comprehensive test oracle for
//! %wor tier parsing equivalence between parsers.
//!
//! # Related CHAT Manual Sections
//!
//! - <https://talkbank.org/0info/manuals/CHAT.html#File_Format>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Main_Tier>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Dependent_Tiers>

use clap::Parser;
use shellexpand::tilde;
use std::{
    collections::BTreeSet,
    error::Error,
    fs::{self, File},
    io::Write,
    path::PathBuf,
};
use walkdir::WalkDir;

use talkbank_model::model::{ChatFile, Line, WorTier, WriteChat};
use talkbank_parser::TreeSitterParser;

/// Extract just the content portion of a WorTier (after %wor:\t).
///
/// The ChatParser::parse_wor_tier method expects content only, not the full tier line.
/// We serialize the full tier and strip the `%wor:\t` prefix.
fn wor_tier_content(tier: &WorTier) -> String {
    let mut full = String::new();
    if let Err(err) = tier.write_chat(&mut full) {
        eprintln!("Failed to write wor tier: {err}");
        return String::new();
    }

    // Strip the "%wor:\t" prefix to get content only
    full.strip_prefix("%wor:\t").unwrap_or(&full).to_string()
}

/// Generate the shared golden %wor tier list from a reference corpus.
#[derive(Parser, Debug)]
#[command(
    name = "generate-golden-wor-tiers",
    about = "Extract known-good %wor tiers from CHAT data modeled by the tree-sitter parser"
)]
/// Command-line arguments for this tool.
struct Args {
    /// Directory containing reference CHAT files (default: corpus/reference).
    #[arg(long, default_value = "corpus/reference")]
    corpus_root: String,

    /// Destination file for the golden %wor tier list.
    #[arg(long, default_value = "talkbank-parser-tests/golden_wor_tiers.txt")]
    output: PathBuf,

    /// Stop after collecting this many unique %wor tiers.
    #[arg(long)]
    limit: Option<usize>,
}

/// Entry point for this binary target.
fn main() -> Result<(), Box<dyn Error>> {
    let args = Args::parse();
    let corpus_root = PathBuf::from(tilde(&args.corpus_root).to_string());

    if !corpus_root.is_dir() {
        return Err(format!(
            "Reference corpus directory does not exist: {}",
            corpus_root.display()
        )
        .into());
    }

    let parser =
        TreeSitterParser::new().map_err(|err| format!("failed to initialize parser: {err}"))?;

    let limit = match args.limit {
        Some(limit) => limit,
        None => usize::MAX,
    };
    let mut wor_tiers = BTreeSet::new();
    let mut file_count = 0;
    let mut error_count = 0;

    for entry in WalkDir::new(&corpus_root)
        .into_iter()
        .filter_map(Result::ok)
        .filter(|entry| entry.file_type().is_file())
        .filter(
            |entry| match entry.path().extension().and_then(|ext| ext.to_str()) {
                Some(ext) => ext.eq_ignore_ascii_case("cha"),
                None => false,
            },
        )
    {
        if wor_tiers.len() >= limit {
            break;
        }

        let path = entry.path();
        file_count += 1;

        let content = match fs::read_to_string(path) {
            Ok(content) => content,
            Err(err) => {
                eprintln!("skipping {}: {err}", path.display());
                error_count += 1;
                continue;
            }
        };

        match parser.parse_chat_file(&content) {
            Ok(chat) => {
                insert_wor_tiers_from_chat(&chat, &mut wor_tiers, limit);
            }
            Err(errs) => {
                eprintln!("parse errors in {}: {errs}", path.display());
                error_count += 1;
            }
        }
    }

    if let Some(parent) = args.output.parent() {
        fs::create_dir_all(parent)?;
    }

    let mut file = File::create(&args.output)?;
    writeln!(
        file,
        "# Generated by `cargo run --bin generate-golden-wor-tiers -- --corpus-root {}`",
        args.corpus_root
    )?;

    for wor_tier_line in wor_tiers.iter().take(limit) {
        writeln!(file, "{wor_tier_line}")?;
    }

    println!("\n=== Golden %wor Tier Extraction Complete ===");
    println!("   Files processed: {}", file_count);
    println!("   Errors: {}", error_count);
    println!(
        "   Golden %wor tier list written to {} ({} tiers)",
        args.output.display(),
        wor_tiers.len().min(limit)
    );

    Ok(())
}

/// Extract %wor tiers from a parsed ChatFile and insert serialized versions into collector.
fn insert_wor_tiers_from_chat(chat: &ChatFile, collector: &mut BTreeSet<String>, limit: usize) {
    for line in chat.lines.iter() {
        if collector.len() >= limit {
            break;
        }

        if let Line::Utterance(utterance) = line {
            if let Some(tier) = utterance.wor_tier() {
                let content = wor_tier_content(tier);
                if !content.is_empty() {
                    collector.insert(content);
                }
            }
        }
    }
}
