//! Generate the shared golden-word corpus from reference CHAT files.
//!
//!
//! # Related CHAT Manual Sections
//!
//! - <https://talkbank.org/0info/manuals/CHAT.html#File_Format>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Main_Tier>
//! - <https://talkbank.org/0info/manuals/CHAT.html#Dependent_Tiers>

use clap::Parser;
use shellexpand::tilde;
use std::{
    collections::BTreeSet,
    error::Error,
    fs::{self, File},
    io::Write,
    path::PathBuf,
};
use walkdir::WalkDir;

use talkbank_model::model::{ChatFile, Line, UtteranceContent};
use talkbank_parser::TreeSitterParser;

/// Generate the shared golden word list from a reference corpus.
#[derive(Parser, Debug)]
#[command(
    name = "generate-golden-words",
    about = "Extract known-good words from CHAT data modeled by the tree-sitter parser"
)]
/// Command-line arguments for this tool.
struct Args {
    /// Directory containing reference CHAT files (default: corpus/reference).
    #[arg(long, default_value = "corpus/reference")]
    corpus_root: String,

    /// Destination file for the golden word list.
    #[arg(long, default_value = "talkbank-parser-tests/golden_words.txt")]
    output: PathBuf,

    /// Stop after collecting this many unique words.
    #[arg(long)]
    limit: Option<usize>,
}

/// Entry point for this binary target.
fn main() -> Result<(), Box<dyn Error>> {
    let args = Args::parse();
    let corpus_root = PathBuf::from(tilde(&args.corpus_root).to_string());

    if !corpus_root.is_dir() {
        return Err(format!(
            "Reference corpus directory does not exist: {}",
            corpus_root.display()
        )
        .into());
    }

    let parser =
        TreeSitterParser::new().map_err(|err| format!("failed to initialize parser: {err}"))?;

    let limit = match args.limit {
        Some(limit) => limit,
        None => usize::MAX,
    };
    let mut words = BTreeSet::new();

    for entry in WalkDir::new(&corpus_root)
        .into_iter()
        .filter_map(Result::ok)
        .filter(|entry| entry.file_type().is_file())
        .filter(
            |entry| match entry.path().extension().and_then(|ext| ext.to_str()) {
                Some(ext) => ext.eq_ignore_ascii_case("cha"),
                None => false,
            },
        )
    {
        if words.len() >= limit {
            break;
        }

        let path = entry.path();
        let content = match fs::read_to_string(path) {
            Ok(content) => content,
            Err(err) => {
                eprintln!("skipping {}: {err}", path.display());
                continue;
            }
        };

        match parser.parse_chat_file(&content) {
            Ok(chat) => {
                insert_words_from_chat(&chat, &mut words, limit);
            }
            Err(errs) => {
                eprintln!("parse errors in {}: {errs}", path.display());
            }
        }
    }

    if let Some(parent) = args.output.parent() {
        fs::create_dir_all(parent)?;
    }

    let mut file = File::create(&args.output)?;
    writeln!(
        file,
        "# Generated by `cargo run --bin generate-golden-words -- --corpus-root {}`",
        args.corpus_root
    )?;

    for word in words.iter().take(limit) {
        writeln!(file, "{word}")?;
    }

    println!(
        "golden word list written to {} ({} words)",
        args.output.display(),
        words.len().min(limit)
    );

    Ok(())
}

/// Insert lexical words extracted from parsed CHAT lines into the set.
fn insert_words_from_chat(chat: &ChatFile, collector: &mut BTreeSet<String>, limit: usize) {
    for line in chat.lines.iter() {
        if collector.len() >= limit {
            break;
        }

        if let Line::Utterance(utterance) = line {
            for content in &utterance.main.content.content {
                if collector.len() >= limit {
                    break;
                }

                match content {
                    UtteranceContent::Word(word) => {
                        collector.insert(word.to_chat());
                    }
                    UtteranceContent::AnnotatedWord(annotated) => {
                        collector.insert(annotated.inner.to_chat());
                    }
                    UtteranceContent::ReplacedWord(replaced) => {
                        collector.insert(replaced.word.to_chat());
                    }
                    _ => {}
                }
            }
        }
    }
}
